const toplama = (pNumber1,pNumber2) => {
    return pNumber1 + pNumber2
};

const cikarma = (pNumber1,pNumber2) => {
    return pNumber1 - pNumber2
};

const carpma = (pNumber1,pNumber2) => {
    return pNumber1 * pNumber2
};

const bolme = (pNumber1,pNumber2) => {
    return pNumber1 / pNumber2
};

module.exports = {
    toplama,
    cikarma,
    carpma,
    bolme,
}